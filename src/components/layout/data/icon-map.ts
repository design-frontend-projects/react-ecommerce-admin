/**
 * Maps the string value stored in `app_screens.icon` to a Lucide icon component so the
 * catalog-driven sidebar can render icons. Keys are lowercased on lookup; unknown values
 * fall back to {@link DEFAULT_NAV_ICON}.
 */
const ICON_MAP: Record<string, LucideIcon> = {
  dashboard: LayoutDashboard,
  layoutdashboard: LayoutDashboard,
  pos: Monitor,
  monitor: Monitor,
  products: Package,
  package: Package,
  boxes: Boxes,
  inventory: Boxes,
  cart: ShoppingCart,
  shoppingcart: ShoppingCart,
  orders: ClipboardList,
  clipboardlist: ClipboardList,
  settings: Settings,
  users: Users,
  usercog: UserCog,
  roles: ShieldCheck,
  shieldcheck: ShieldCheck,
  permissions: ShieldCheck,
  screens: Grid3X3,
  grid: Grid3X3,
  buttons: Wrench,
  wrench: Wrench,
  reports: LineChart,
  analytics: LineChart,
  linechart: LineChart,
  payments: CreditCard,
  creditcard: CreditCard,
  wallet: Wallet,
  receipt: Receipt,
  transactions: DollarSign,
  dollarsign: DollarSign,
  kitchen: ChefHat,
  chefhat: ChefHat,
  menu: UtensilsCrossed,
  utensilscrossed: UtensilsCrossed,
  reservations: CalendarClock,
  calendarclock: CalendarClock,
  shifts: Timer,
  timer: Timer,
  shipments: Truck,
  truck: Truck,
  transfers: ArrowLeftRight,
  arrowleftright: ArrowLeftRight,
  suppliers: Building2,
  building: Building2,
  branches: MapPin,
  mappin: MapPin,
  audit: History,
  history: History,
  notifications: Bell,
  bell: Bell,
  help: HelpCircle,
}

export const DEFAULT_NAV_ICON: LucideIcon = HelpCircle

export function resolveNavIcon(icon: string | null | undefined): LucideIcon {
  if (!icon) return DEFAULT_NAV_ICON
  return ICON_MAP[icon.trim().toLowerCase()] ?? DEFAULT_NAV_ICON
}
